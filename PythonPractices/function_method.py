def sum_function(first_number, second_number) -> int:
    addition = first_number + second_number
    return addition


def multiplication_function(first_number, second_number) -> float:
    multiplication = first_number * second_number
    return multiplication


def division_function(first_number, second_number) -> int:
    division = first_number // second_number
    return division


def subtraction_function(first_number, second_number) -> int:
    subtraction = first_number - second_number
    return subtraction


