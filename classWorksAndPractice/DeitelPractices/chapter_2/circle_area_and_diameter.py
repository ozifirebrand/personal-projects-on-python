pi = 3.14159
circle_radius = float(input("What is circle's radius in cm?: "))
diameter = 2 * circle_radius
area_of_circle = pi * circle_radius ** 2
circumference_of_circle = 2 * pi * circle_radius

print('Circle radius is', circle_radius, 'cm')
print('Circle diameter is', diameter, 'cm')
print('Circle area is', area_of_circle, 'cm')
print('Circle circumference is', circumference_of_circle, 'cm')

